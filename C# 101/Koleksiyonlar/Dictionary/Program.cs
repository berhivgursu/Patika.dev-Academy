using System;
using System.Collections.Generic;
using System.IO;
class Solution {
    static void Main(String[] args) {
        
        Dictionary<string,int> phoneBook = new Dictionary<string, int>();

        int a = int.Parse(Console.ReadLine());

        for (int i = 0; i < a; i++)
